const nilai1 = 10;
const nilai2 = 20;

console.log(`Apakah ${nilai1} > ${nilai2}?`, nilai1 > nilai2);
console.log(`Apakah ${nilai1} >= ${nilai2}?`, nilai1 >= nilai2);
console.log(`Apakah ${nilai1} == ${nilai2}?`, nilai1 == nilai2);
console.log(`Apakah ${nilai1} === ${nilai2}?`, nilai1 === nilai2);
console.log(`Apakah ${nilai1} !== ${nilai2}?`, nilai1 !== nilai2);

// Membandingkan angka 10 dengan string "10" menggunakan operator < dan >
const angka = 10;
const stringAngka = "10";

console.log(`Apakah ${angka} < "${stringAngka}"?`, angka < stringAngka);
console.log(`Apakah ${angka} > "${stringAngka}"?`, angka > stringAngka);
