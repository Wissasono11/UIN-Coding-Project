let x = 10;

console.log(`Nilai awal x: ${x}`);

// Menggunakan operator assignment +=
x += 5;
console.log(`Nilai x setelah += 5: ${x}`);

// Menggunakan operator assignment *=
x *= 2;
console.log(`Nilai x setelah *= 2: ${x}`);

// Menggunakan operator assignment %=
x %= 3;
console.log(`Nilai x setelah %= 3: ${x}`);
